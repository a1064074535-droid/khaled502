// دالة توليد رسالة واتساب من بيانات الوظيفة
export function generateWhatsAppLink(job: Job): string {
  const message = `السلام عليكم،

أرغب في الاستفسار عن الوظيفة التالية:

📋 *المسمى الوظيفي:* ${job.title}
🏢 *الشركة:* ${job.company}
📍 *الموقع:* ${job.location}
⏰ *نوع العمل:* ${job.type}
📱 *المصدر:* ${job.source}

شكراً لكم`;

  return `https://wa.me/${WHATSAPP_NUMBER}?text=${encodeURIComponent(message)}`;
}

